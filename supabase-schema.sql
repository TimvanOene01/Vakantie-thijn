create extension if not exists pgcrypto;

create table if not exists public.site_settings (
  key text primary key,
  value jsonb not null default '{}'::jsonb
);

create table if not exists public.photo_entries (
  id uuid primary key default gen_random_uuid(),
  display_order integer not null,
  tag text not null,
  title text not null,
  copy text not null default '',
  note text not null default '',
  image text not null,
  comment_id text not null unique,
  active boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.photo_comments (
  id bigint generated by default as identity primary key,
  photo_id text not null,
  author text not null default 'anoniem',
  body text not null,
  created_at timestamptz not null default now(),
  constraint photo_comments_author_length check (char_length(author) between 1 and 32),
  constraint photo_comments_body_length check (char_length(body) between 1 and 320)
);

create table if not exists public.page_visits (
  id bigint generated by default as identity primary key,
  session_id text not null,
  path text not null default '/',
  created_at timestamptz not null default now()
);

create table if not exists public.admin_users (
  username text primary key,
  password_hash text not null
);

create table if not exists public.admin_sessions (
  token text primary key,
  username text not null references public.admin_users(username) on delete cascade,
  created_at timestamptz not null default now(),
  expires_at timestamptz not null
);

create or replace function public.current_admin_token()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(
    nullif(current_setting('request.headers', true), '')::jsonb ->> 'x-admin-token',
    ''
  );
$$;

