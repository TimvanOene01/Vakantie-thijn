create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select
    exists (
      select 1
      from public.admin_sessions
      where token = public.current_admin_token()
        and expires_at > now()
    )
    or exists (
      select 1
      from public.admin_users
      where username = public.current_admin_username()
        and password_hash = crypt(public.current_admin_password(), password_hash)
    );
$$;

create or replace function public.admin_login(p_username text, p_password text)
returns text
language plpgsql
security definer
set search_path = public
as $$
declare
  stored_hash text;
  new_token text;
begin
  select password_hash
  into stored_hash
  from public.admin_users
  where username = p_username;

  if stored_hash is null or stored_hash <> crypt(p_password, stored_hash) then
    raise exception 'invalid login';
  end if;

  new_token := gen_random_uuid()::text;

  insert into public.admin_sessions (token, username, expires_at)
  values (new_token, p_username, now() + interval '30 days');

  return new_token;
end;
$$;

